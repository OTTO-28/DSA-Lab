#define int long long int
#define vi vector<int>
#define vpii vector<pair<int,int>>
#define yes cout<<"YES"<<endl;
#define no cout<<"NO"<<endl;
#define nl endl;
#define READ(a,n) for(int i=0;i<n;i++)cin>>a[i];

const int mod=1e9+7;

signed main() {
  ios::sync_with_stdio(false);
  cin.tie(0);

  int n,C;
  cin>>n>>C;
  vector<pair<int,int>>a(n);
  for(int i=0;i<n;i++){
    cin>>a[i].first; //value
    cin>>a[i].second; //weight
  }

  sort(a.begin(),a.end(),[](const pair<int,int> &p1, const pair<int,int> &p2)->bool{
    double d1=(double)(p1.first)/p1.second;
    double d2=(double)(p2.first)/p2.second;
    return d1>d2;
  });

  double ans=0;
  int curr=0;
  for(int i=0;i<n;i++){
    if(curr+a[i].second<=C){
      ans+=a[i].first;
      curr+=a[i].second;
    }
    else{
      ans +=((double)(C-curr)/a[i].second)*a[i].first;
    }
  }

  cout<<ans<<endl;

  return 0;
}
