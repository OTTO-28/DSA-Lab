#include <bits/stdc++.h>
using namespace std;

struct job{
  int id;
  int deadline;
  int profit;
};

signed main() {
  ios::sync_with_stdio(false);
  cin.tie(0);

  int n;
  cin>>n;
  vector<job> jobs(n);
  for(int i=0;i<n;i++){
    jobs[i].id=i+1;
    cin>>jobs[i].profit;
    cin>>jobs[i].deadline;
  }

  sort(jobs.begin(),jobs.end(),[](const job &J1,const job &J2)->bool{
    return J1.profit>J2.profit;
  });

  vector<int>ans(n);
  vector<bool> slots(n,false);

  for(int i=0;i<n;i++){
    for(int j=min(n,jobs[i].deadline)-1;j>=0;j--){
      if(slots[j]==false){
        ans[j]=i;
        slots[j]=true;
        break;
      }
    }
  }

  for(int i=0;i<n;i++){
    if(slots[i]){
      cout<<jobs[ans[i]].id<<" ";
    }
  }
  cout<<endl;












  return 0;
}
