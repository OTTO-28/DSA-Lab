#include <bits/stdc++.h>
using namespace std;

void merge(vector<int>&a,int l,int mid,int r){
  int size1=mid-l+1,size2=r-mid;
  vector<int>left(size1),right(size2);

  for(int i=0;i<size1;i++){
    left[i]=a[i+l];
  }
  for(int j=0;j<size2;j++){
    right[j]=a[j+mid+1];
  }

  int i=0,j=0,k=l;

  while(i<size1&&j<size2){
    if(left[i]<=right[j]){
      a[k++]=left[i++];
    }
    else{
      a[k++]=right[j++];
    }
  }

  while(i<size1){
    a[k++]=left[i++];
  }

  while(j<size2){
    a[k++]=right[j++];
  }

}

void mergesort(vector<int>&a,int l,int r){
  if(l>=r){
    return;
  }

  int mid=l+(r-l)/2;
  mergesort(a,l,mid);
  mergesort(a,mid+1,r);
  merge(a,l,mid,r);
}

signed main() {
  ios::sync_with_stdio(false);
  cin.tie(0);

  int n;cin>>n;
  vector<int>a(n);
  for(int i=0;i<n;i++){
    cin>>a[i];
  }

  mergesort(a,0,n-1);

  for(int i=0;i<n;i++){
    cout<<a[i]<<" ";
  }
  cout<<endl;


  return 0;
}
