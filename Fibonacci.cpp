#include <bits/stdc++.h>
using namespace std;

int fibonacci(int n){
  if(n==0||n==1){
    return n;
  }
  return fibonacci(n-1)+fibonacci(n-2);
}

signed main() {
  ios::sync_with_stdio(false);
  cin.tie(0);

  cout<<"The first 10 fibonacci numbers are: "<<endl;
  for(int i=0;i<10;i++){
    cout<<fibonacci(i)<<" ";
  }
  cout<<endl;

  return 0;
}
