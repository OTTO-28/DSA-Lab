#include <bits/stdc++.h>
using namespace std;

signed main() {
  ios::sync_with_stdio(false);
  cin.tie(0);

  int ans=INT_MIN,pins=0,earrings=0;
  for(int x=0;x<=40;x++){
    for(int y=0;y<=20;y++){
      if(x>=y && x+2*y<=40 && x+y>=21 ){
        if(8*x+20*y>ans){
          pins=x;
          earrings=y;
        }
        ans=max(ans,8*x+20*y);
      }
    }
  }

  cout<<pins<<" "<<earrings<<" "<<ans<<endl;
  
  return 0;
}
